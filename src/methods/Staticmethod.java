package methods;

public class Staticmethod {

	//the method which is declared with the help of static keyword is called static method
	// static method calling directly
	
	
	static void money() {
		System.out.println("Akash");
		
	}
	
	 static void bank() {
		 
		System.out.println("bank");
	}
	
	
	
	public static void main(String[] args) {
		
		money();
		bank();
		

	}

}
